void main() {
  //   Switch Case Statements applicable for only int and String

  String grade = 'A';

  switch (grade) {
    case 'A':
      print('Excellent grade of A');
      break;
    case 'B':
      print('Very Good!');
      break;
    case 'C':
      print('Good, but work hard');
      break;
    case 'F':
      print('You have failed!');
      break;
    default:
      print('Invalid Grade');
  }
}